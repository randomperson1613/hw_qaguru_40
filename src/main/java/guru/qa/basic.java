package guru.qa;

public class basic {
    public static void main(String[] args) {
        int a = 17;
        int b = 5;
        double c = 3.33;
        double d = Double.MAX_VALUE;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);
        System.out.println(a / c);
        System.out.println(a * c);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
        System.out.println(d * d);
    }

}
